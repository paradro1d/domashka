def letter(m,n):
    for i in range(len(m[n])):
        if i % 2 == 0:
            tr.left(m[n][i])
        else:
            tr.forward(m[n][i])
def text(m,a):
    tr.left(180)
    for i in range(len(a)):
        letter(m,a[i])
        tr.penup()
        tr.forward(20)
        tr.left(180)
        tr.pendown()
